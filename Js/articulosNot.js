// Obtener parámetro de la URL
function obtenerParametro(param) {
    const urlParams = new URLSearchParams(window.location.search);
    return urlParams.get(param);
}

document.addEventListener("DOMContentLoaded", () => {
    const progKey = obtenerParametro("programa");
    const data = programas[progKey];

    if (!data) {
        document.getElementById("contenido-programa").innerHTML = "<p>Programa no encontrado.</p>";
        return;
    }

    document.getElementById("titulo-programa").textContent = data.titulo;
    document.getElementById("imagen-programa").src = data.imagen;
    document.getElementById("imagen-programa").alt = data.titulo;
    document.getElementById("descripcion-programa").innerHTML =
        data.descripcion.replace(/\n/g, "<br>");
    document.getElementById("horario-programa").innerHTML =
        data.horario.replace(/\n/g, "<br>");

    // Navegación
    const keys = Object.keys(programas);
    const indexActual = keys.indexOf(progKey);

    const btnAnterior = document.getElementById("btn-anterior");
    const btnSiguiente = document.getElementById("btn-siguiente");

    // Programa anterior
    if (indexActual > 0) {
        const keyAnterior = keys[indexActual - 1];
        btnAnterior.href = `?programa=${keyAnterior}`;
        btnAnterior.innerHTML = `← Artículo anterior <span>${programas[keyAnterior].titulo}</span>`;
    } else {
        btnAnterior.classList.add("oculto");
    }

    // Programa siguiente
    if (indexActual < keys.length - 1) {
        const keySiguiente = keys[indexActual + 1];
        btnSiguiente.href = `?programa=${keySiguiente}`;
        btnSiguiente.innerHTML = `Artículo siguiente → <span>${programas[keySiguiente].titulo}</span>`;
    } else {
        btnSiguiente.classList.add("oculto");
    }
});

